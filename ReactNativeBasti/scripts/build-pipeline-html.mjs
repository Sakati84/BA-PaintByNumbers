import * as esbuild from "esbuild";
import { mkdir, readFile, rm, writeFile } from "node:fs/promises";
import { dirname, resolve } from "node:path";
import { fileURLToPath } from "node:url";

const here = dirname(fileURLToPath(import.meta.url));
const appRoot = resolve(here, "..");
const repoRoot = resolve(appRoot, "..");
const entryPoint = resolve(appRoot, "src/features/pipeline/pipelineBridgeStandalone.ts");
const opencvPath = resolve(repoRoot, "react-app/node_modules/@techstark/opencv-js/dist/opencv.js");
const outDir = resolve(appRoot, "src/features/pipeline/generated");
const tempBundle = resolve(appRoot, ".pipeline-html-build/pipelineBridgeStandalone.js");
const outFile = resolve(outDir, "pipelineAssets.ts");

await rm(resolve(appRoot, ".pipeline-html-build"), { recursive: true, force: true });
await mkdir(dirname(tempBundle), { recursive: true });
await mkdir(outDir, { recursive: true });

await esbuild.build({
  entryPoints: [entryPoint],
  bundle: true,
  outfile: tempBundle,
  format: "iife",
  platform: "browser",
  target: "es2020",
  legalComments: "none",
});

const [opencvJs, bridgeJs] = await Promise.all([
  readFile(opencvPath, "utf8"),
  readFile(tempBundle, "utf8"),
]);

const html = `<!doctype html>
<html lang="en">
  <head>
    <meta charset="utf-8" />
    <meta name="viewport" content="width=device-width, initial-scale=1" />
    <title>Local Paint Pipeline</title>
    <style>
      html, body { width: 100%; height: 100%; margin: 0; overflow: hidden; background: #fff; }
    </style>
  </head>
  <body>
    <script>
      (function () {
        function post(message) {
          try {
            if (window.ReactNativeWebView && window.ReactNativeWebView.postMessage) {
              window.ReactNativeWebView.postMessage(JSON.stringify(message));
            }
          } catch (error) {}
        }

        function status(message) {
          post({ type: "STATUS", message: message });
        }

        window.addEventListener("error", function (event) {
          post({
            type: "ERROR",
            message: "Hidden WebView JS error: " + (event.message || "Unknown script error."),
            stack: event.error && event.error.stack,
          });
        });

        window.addEventListener("unhandledrejection", function (event) {
          var reason = event.reason;
          post({
            type: "ERROR",
            message:
              "Hidden WebView unhandled rejection: " +
              (reason && reason.message ? reason.message : String(reason || "unknown rejection")),
            stack: reason && reason.stack,
          });
        });

        if (typeof window.OffscreenCanvas === "undefined") {
          window.OffscreenCanvas = function OffscreenCanvas(width, height) {
            var canvas = document.createElement("canvas");
            canvas.width = width;
            canvas.height = height;
            return canvas;
          };
        }

        window.__paintOpenCvRuntime = {
          ready: false,
          cv: null,
          callbacks: [],
          onReady: function (callback) {
            if (this.ready) {
              callback(this.cv);
              return;
            }
            this.callbacks.push(callback);
          },
          resolve: function (cvInstance) {
            this.ready = true;
            this.cv = cvInstance;
            status("OpenCV runtime ready");
            while (this.callbacks.length > 0) {
              this.callbacks.shift()(cvInstance);
            }
          },
        };

        var Module = {
          print: function (message) {
            status("OpenCV: " + message);
          },
          printErr: function (message) {
            status("OpenCV warning: " + message);
          },
          monitorRunDependencies: function (left) {
            status(left > 0 ? "OpenCV loading dependencies: " + left : "OpenCV dependencies loaded");
          },
          onAbort: function (reason) {
            post({ type: "ERROR", message: "OpenCV abort: " + String(reason || "unknown") });
          },
          onRuntimeInitialized: function () {
            var cvInstance = this;
            setTimeout(function () {
              window.__paintOpenCvRuntime.resolve(cvInstance);
            }, 0);
          },
        };

        window.Module = Module;
        status("Loading OpenCV script");
      })();
    </script>
    <script src="./opencv.js"></script>
    <script src="./pipelineBridge.js"></script>
    <script>
      window.__paintOpenCvRuntime.onReady(function (cvInstance) {
        window.PaintPipelineBridge.initializeOpenCv(cvInstance);
      });
    </script>
  </body>
</html>`;

await writeFile(
  outFile,
  [
    "// Generated by scripts/build-pipeline-html.mjs. Do not edit by hand.",
    `export const COMPUTE_HTML = ${JSON.stringify(html)};`,
    `export const OPENCV_JS = ${JSON.stringify(opencvJs)};`,
    `export const PIPELINE_BRIDGE_JS = ${JSON.stringify(bridgeJs)};`,
    "",
  ].join("\n"),
);

console.log(`Wrote ${outFile}`);

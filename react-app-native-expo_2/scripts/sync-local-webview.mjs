import { execSync } from 'node:child_process';
import { createHash } from 'node:crypto';
import { mkdir, readdir, readFile, writeFile } from 'node:fs/promises';
import path from 'node:path';
import { fileURLToPath } from 'node:url';

const __filename = fileURLToPath(import.meta.url);
const __dirname = path.dirname(__filename);
const projectRoot = path.resolve(__dirname, '..');
const repoRoot = path.resolve(projectRoot, '..');
const reactAppRoot = path.join(repoRoot, 'react-app');
const reactAppDist = path.join(reactAppRoot, 'dist');
const outputFile = path.join(projectRoot, 'src', 'features', 'generator', 'localWebViewManifest.generated.ts');

const BASE64_CHUNK_SIZE = 120_000;

run();

async function run() {
  buildLocalWebView();

  const files = await collectFiles(reactAppDist);
  if (files.length === 0) {
    throw new Error(`No files found in ${reactAppDist}.`);
  }

  const hash = createHash('sha1');
  const manifestEntries = [];

  for (const absolutePath of files) {
    const relativePath = toPosix(path.relative(reactAppDist, absolutePath));
    const fileBytes = await readFile(absolutePath);
    const base64 = fileBytes.toString('base64');

    hash.update(relativePath);
    hash.update(fileBytes);

    manifestEntries.push({
      relativePath,
      byteLength: fileBytes.byteLength,
      base64Chunks: splitIntoChunks(base64, BASE64_CHUNK_SIZE),
    });
  }

  const buildId = hash.digest('hex');
  const source = createManifestSource(buildId, manifestEntries);

  await mkdir(path.dirname(outputFile), { recursive: true });
  await writeFile(outputFile, source, 'utf8');

  console.log(`Wrote local WebView manifest with ${manifestEntries.length} files to ${outputFile}`);
}

function buildLocalWebView() {
  execSync('npm run build:webview-local', {
    cwd: reactAppRoot,
    stdio: 'inherit',
    shell: true,
  });
}

async function collectFiles(directory) {
  const dirents = await readdir(directory, { withFileTypes: true });
  const nested = await Promise.all(
    dirents
      .sort((left, right) => left.name.localeCompare(right.name))
      .map(async (dirent) => {
        const fullPath = path.join(directory, dirent.name);
        if (dirent.isDirectory()) {
          return collectFiles(fullPath);
        }
        return [fullPath];
      }),
  );

  return nested.flat();
}

function splitIntoChunks(value, chunkSize) {
  if (value.length <= chunkSize) {
    return [value];
  }

  const chunks = [];
  for (let offset = 0; offset < value.length; offset += chunkSize) {
    chunks.push(value.slice(offset, offset + chunkSize));
  }
  return chunks;
}

function toPosix(value) {
  return value.split(path.sep).join('/');
}

function createManifestSource(buildId, files) {
  const serializedEntries = files
    .map((file) => {
      const serializedChunks = file.base64Chunks.map((chunk) => JSON.stringify(chunk)).join(', ');
      return `  {\n    relativePath: ${JSON.stringify(file.relativePath)},\n    byteLength: ${file.byteLength},\n    base64Chunks: [${serializedChunks}],\n  }`;
    })
    .join(',\n');

  return `// This file is auto-generated by scripts/sync-local-webview.mjs.\n` +
    `// Do not edit manually.\n\n` +
    `export type LocalWebViewBundledFile = {\n` +
    `  relativePath: string;\n` +
    `  byteLength: number;\n` +
    `  base64Chunks: string[];\n` +
    `};\n\n` +
    `export const LOCAL_WEBVIEW_BUILD_ID = ${JSON.stringify(buildId)};\n\n` +
    `export const LOCAL_WEBVIEW_FILES: LocalWebViewBundledFile[] = [\n${serializedEntries}\n];\n`;
}
